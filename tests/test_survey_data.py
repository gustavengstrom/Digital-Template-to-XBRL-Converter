# XBRL qnames from report-facts.json that are intentionally NOT represented
# as individual survey questions.  Each is paired with the qname that IS in
# the survey and covers the same column (the unit dropdown determines which
# interpretation applies at runtime).
#
# When the dedup strategy changes, update this dict accordingly.
_COLUMN_DEDUP_COLLAPSED: dict[str, str] = {
    # b7_annual_mass_flow: Volume collapsed into Weight (same "mass_volume" column)
    "vsme:VolumeOfMaterialUsed": "vsme:WeightOfMaterialUsed",
    # b7_waste_generated: Mass/Volume alternates within same column
    "vsme:WasteDivertedToRecycleOrReuseMass": "vsme:WasteDivertedToRecycleOrReuseVolume",
    "vsme:WasteDirectedToDisposalVolume": "vsme:WasteDirectedToDisposalMass",
    "vsme:TotalWasteRecycledReusedAndDirectedToDisposalMass": "vsme:TotalWasteRecycledReusedAndDirectedToDisposalVolume",
}


class TestXbrlSurveyCoverage:
    """Every XBRL fact qname in report-facts.json must appear in the
    survey output (via ``_meta.xbrl_qname``), or be a known column-dedup
    collapse whose counterpart IS in the survey.

    This catches regressions where gen-survey-json.py accidentally drops
    an XBRL concept from the survey output.
    """

    @pytest.fixture(scope="class")
    def report_xbrl_qnames(self, report) -> set[str]:
        """Unique XBRL qnames across all report-facts sections."""
        qnames: set[str] = set()
        for section in report.get("sections", []):
            for field in section.get("fields", []):
                qn = field.get("qname", "")
                if qn:
                    qnames.add(qn)
        return qnames

    @pytest.fixture(scope="class")
    def survey_xbrl_qnames(self, combined) -> set[str]:
        """Unique XBRL qnames across all survey questions."""
        qnames: set[str] = set()
        for section in combined:
            for q in section.get("survey_data_proxy", []):
                xq = q.get("_meta", {}).get("xbrl_qname", "")
                if xq:
                    qnames.add(xq)
        return qnames

    def test_report_qname_count(self, report_xbrl_qnames):
        """Guard-rail: expected number of unique XBRL qnames in report-facts."""
        assert len(report_xbrl_qnames) == 141

    def test_survey_qname_count(self, survey_xbrl_qnames):
        """Guard-rail: expected number of unique XBRL qnames in survey."""
        assert len(survey_xbrl_qnames) == 137

    def test_collapsed_count(self):
        """Guard-rail: the number of known column-dedup collapsed qnames."""
        assert len(_COLUMN_DEDUP_COLLAPSED) == 4

    def test_all_report_qnames_covered_by_survey(
        self, report_xbrl_qnames, survey_xbrl_qnames
    ):
        """Every XBRL qname from report-facts must be in the survey or in
        the known column-dedup collapsed set."""
        uncovered = []
        for qn in sorted(report_xbrl_qnames):
            if qn in survey_xbrl_qnames:
                continue
            if qn in _COLUMN_DEDUP_COLLAPSED:
                continue
            uncovered.append(qn)

        if uncovered:
            detail = "\n".join(f"  {qn}" for qn in uncovered)
            pytest.fail(
                f"{len(uncovered)} XBRL qname(s) from report-facts are not "
                f"represented in the survey output:\n{detail}\n\n"
                f"If intentionally collapsed by column-dedup, add them to "
                f"_COLUMN_DEDUP_COLLAPSED with their surviving counterpart."
            )

    def test_collapsed_counterparts_are_in_survey(self, survey_xbrl_qnames):
        """Every collapsed qname's counterpart must actually be in the survey."""
        missing_counterparts = []
        for collapsed, survivor in _COLUMN_DEDUP_COLLAPSED.items():
            if survivor not in survey_xbrl_qnames:
                missing_counterparts.append((collapsed, survivor))

        if missing_counterparts:
            detail = "\n".join(
                f"  {c} → counterpart {s} NOT in survey"
                for c, s in missing_counterparts
            )
            pytest.fail(
                f"{len(missing_counterparts)} collapsed qname(s) have "
                f"counterparts missing from the survey:\n{detail}"
            )

    def test_collapsed_are_actually_missing(
        self, report_xbrl_qnames, survey_xbrl_qnames
    ):
        """Every entry in _COLUMN_DEDUP_COLLAPSED must genuinely be absent
        from the survey.  If the qname starts appearing, remove it from the
        dict."""
        false_collapsed = [
            qn
            for qn in _COLUMN_DEDUP_COLLAPSED
            if qn in survey_xbrl_qnames
        ]
        if false_collapsed:
            detail = "\n".join(f"  {qn}" for qn in false_collapsed)
            pytest.fail(
                f"{len(false_collapsed)} _COLUMN_DEDUP_COLLAPSED entries are "
                f"now in the survey — remove them:\n{detail}"
            )

    def test_no_survey_qname_outside_report(
        self, report_xbrl_qnames, survey_xbrl_qnames
    ):
        """Every XBRL qname in the survey must originate from report-facts.
        Synthetic fields (no qname) are excluded from this check."""
        extra = survey_xbrl_qnames - report_xbrl_qnames
        if extra:
            detail = "\n".join(f"  {qn}" for qn in sorted(extra))
            pytest.fail(
                f"{len(extra)} XBRL qname(s) in survey have no corresponding "
                f"field in report-facts:\n{detail}"
            )
