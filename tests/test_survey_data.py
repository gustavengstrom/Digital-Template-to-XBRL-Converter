class TestB7MassFlowSurvey:
    """Survey output tests for the b7_annual_mass_flow section.

    This section has a yellow-cell trigger, an expandable table
    (with additional_rows_warning), and summary/total rows.
    """

    @pytest.fixture(scope="class")
    def mass_flow(self):
        return _load_section_survey(
            "b7_annual_mass_flow_of_relevant_materials_used"
        )

    @pytest.fixture(scope="class")
    def questions(self, mass_flow):
        return mass_flow["survey_data_proxy"]

    def test_question_count(self, questions):
        """6 questions: 1 trigger + 3 dynamic table (name, mass/vol, unit)
        + 2 summary totals.  Mass/Volume is a single item (Weight + Volume
        share the same column label and are collapsed)."""
        assert len(questions) == 6

    def test_trigger_is_first(self, questions):
        """The yellow-cell trigger question must be first."""
        meta = questions[0].get("_meta", {})
        assert meta.get("source") == "yellowCell"
        assert questions[0]["answer_type"] == "RADIO"

    def test_table_data_before_summaries(self, questions):
        """Table data fields (NameOfMaterialUsed, Weight/Volume) must appear
        before summary/total fields (TotalMass/Volume).  After column-label
        dedup, only WeightOfMaterialUsed survives (VolumeOfMaterialUsed
        shares the same column and is collapsed)."""
        table_qnames = {
            "vsme:NameOfMaterialUsed",
            "vsme:WeightOfMaterialUsed",
        }
        summary_qnames = {
            "vsme:TotalMassOfMaterialUsed",
            "vsme:TotalVolumeOfMaterialUsed",
        }

        table_indices = [
            i
            for i, q in enumerate(questions)
            if q.get("_meta", {}).get("xbrl_qname") in table_qnames
        ]
        summary_indices = [
            i
            for i, q in enumerate(questions)
            if q.get("_meta", {}).get("xbrl_qname") in summary_qnames
        ]

        assert len(table_indices) >= 2
        assert len(summary_indices) == 2
        assert max(table_indices) < min(summary_indices), (
            f"Table fields at {table_indices} should all precede "
            f"summary fields at {summary_indices}"
        )

    def test_no_display_only_unit_fields(self, questions):
        """Display-only unit fields (TotalMass/VolumeOfMaterialUsed_unit)
        should be excluded — they have no options."""
        unit_field_ids = [
            q.get("_meta", {}).get("source_field_id", "")
            for q in questions
        ]
        assert "TotalMassOfMaterialUsed_unit" not in unit_field_ids
        assert "TotalVolumeOfMaterialUsed_unit" not in unit_field_ids

    def test_unit_selection_has_options(self, questions):
        """The shared unit selection field must have answer options."""
        unit_qs = [
            q for q in questions if q["answer_type"] == "DROPDOWN"
            and q.get("answer_options")
        ]
        assert len(unit_qs) >= 1, "Expected at least 1 DROPDOWN with options"

    def test_summary_totals_are_numeric(self, questions):
        """TotalMass and TotalVolume fields must be NUMERIC."""
        summary_qnames = {
            "vsme:TotalMassOfMaterialUsed",
            "vsme:TotalVolumeOfMaterialUsed",
        }
        summaries = [
            q
            for q in questions
            if q.get("_meta", {}).get("xbrl_qname") in summary_qnames
        ]
        assert len(summaries) == 2
        for q in summaries:
            assert q["answer_type"] == "NUMERIC"

    def test_no_duplicate_names(self, questions):
        """No two questions should share the same name."""
        from collections import Counter

        names = Counter(q["name"] for q in questions)
        dupes = {n: c for n, c in names.items() if c > 1}
        assert dupes == {}, f"Duplicate question names: {dupes}"

    def test_mass_volume_is_single_item(self, questions):
        """The 'Mass / Volume' column must produce exactly one survey
        question (Weight + Volume share the same column label and the
        unit dropdown determines interpretation)."""
        mass_vol_qs = [
            q for q in questions if q["name"] == "Mass / Volume"
        ]
        assert len(mass_vol_qs) == 1, (
            f"Expected 1 'Mass / Volume' question, got {len(mass_vol_qs)}"
        )

    def test_dynamic_group_on_table_fields(self, questions):
        """Table fields (name, mass/volume, unit) must be in a dynamic
        group; trigger and summary totals must not."""
        dynamic_qs = [q for q in questions if "__DQ0" in q["id"]]
        static_qs = [q for q in questions if "__DQ0" not in q["id"]]

        # 3 dynamic: Name, Mass/Volume, Unit
        assert len(dynamic_qs) == 3, (
            f"Expected 3 dynamic questions, got {len(dynamic_qs)}: "
            f"{[q['name'] for q in dynamic_qs]}"
        )
        # All share the same group_id
        group_ids = {q["dynamic_group_id"] for q in dynamic_qs}
        assert len(group_ids) == 1

        # 3 static: trigger + 2 totals
        assert len(static_qs) == 3
        # Trigger is the yellow cell
        assert static_qs[0].get("_meta", {}).get("source") == "yellowCell"

    def test_dynamic_group_header(self, questions):
        """Dynamic group header should be 'Material'."""
        dynamic_qs = [q for q in questions if "__DQ0" in q["id"]]
        for q in dynamic_qs:
            assert q["dynamic_header"] == "Material"

    def test_dynamic_tag_expression(self, questions):
        """Tag expression should reference the NameOfMaterialUsed field."""
        dynamic_qs = [q for q in questions if "__DQ0" in q["id"]]
        tag_exprs = {q.get("dynamic_tag_expression", "") for q in dynamic_qs}
        # All should have the same tag expression
        assert len(tag_exprs) == 1
        tag_expr = tag_exprs.pop()
        assert tag_expr, "dynamic_tag_expression should not be empty"
        # Should reference a DQ0 ID
        assert "__DQ0" in tag_expr


