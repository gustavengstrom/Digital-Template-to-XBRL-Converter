def _load_section_survey(section_id: str) -> dict:
    """Load a per-section survey JSON file."""
    # Search for the file in all sheet subdirectories
    for path in SURVEYS_DIR.rglob(f"{section_id}.json"):
        with open(path, encoding="utf-8") as f:
            return json.load(f)
    pytest.skip(f"Section file {section_id}.json not found under {SURVEYS_DIR}")


