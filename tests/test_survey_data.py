class TestConditionCriteriaPairing:
    """condition and condition_criteria must always appear as a pair.

    Per survey_data.instructions.md, ``condition`` defines *when* a question
    is shown and ``condition_criteria`` defines *which values* of the
    referenced question(s) satisfy the condition.  One without the other is
    a bug — a condition without criteria cannot be evaluated, and criteria
    without a condition have no anchor.
    """

    def test_no_condition_without_criteria_combined(self, all_questions):
        """Every question with 'condition' in survey_data_all.json must also
        have 'condition_criteria'."""
        violations = [
            q["id"]
            for q in all_questions
            if "condition" in q and "condition_criteria" not in q
        ]
        assert violations == [], (
            f"{len(violations)} question(s) have 'condition' but no "
            f"'condition_criteria': {violations}"
        )

    def test_no_criteria_without_condition_combined(self, all_questions):
        """Every question with 'condition_criteria' in survey_data_all.json
        must also have 'condition'."""
        violations = [
            q["id"]
            for q in all_questions
            if "condition_criteria" in q and "condition" not in q
        ]
        assert violations == [], (
            f"{len(violations)} question(s) have 'condition_criteria' but no "
            f"'condition': {violations}"
        )

    def test_no_condition_without_criteria_per_section(self, per_section_questions):
        """Every question with 'condition' in per-section files must also
        have 'condition_criteria'."""
        violations = [
            q["id"]
            for q in per_section_questions
            if "condition" in q and "condition_criteria" not in q
        ]
        assert violations == [], (
            f"{len(violations)} question(s) have 'condition' but no "
            f"'condition_criteria': {violations}"
        )

    def test_no_criteria_without_condition_per_section(self, per_section_questions):
        """Every question with 'condition_criteria' in per-section files must
        also have 'condition'."""
        violations = [
            q["id"]
            for q in per_section_questions
            if "condition_criteria" in q and "condition" not in q
        ]
        assert violations == [], (
            f"{len(violations)} question(s) have 'condition_criteria' but no "
            f"'condition': {violations}"
        )

    def test_condition_and_criteria_counts_match(self, all_questions):
        """The number of questions with 'condition' must equal those with
        'condition_criteria'."""
        with_cond = sum(1 for q in all_questions if "condition" in q)
        with_crit = sum(1 for q in all_questions if "condition_criteria" in q)
        assert with_cond == with_crit, (
            f"Mismatch: {with_cond} questions with 'condition' vs "
            f"{with_crit} with 'condition_criteria'"
        )


