"""
Tests for the survey JSON output produced by gen-survey-json.py.

Run with:
    conda run -n p312 python -m pytest tests/test_survey_data.py -v

Purpose
-------
These tests verify that the survey_data_proxy JSON files generated by
gen-survey-json.py satisfy the structural invariants described in
.github/instructions/survey_data.instructions.md.

Every assertion below can be verified by inspecting the generated
output/surveys/ files.  They are intentionally coarse-grained so that they
catch regressions caused by:
  - Changes to gen-survey-json.py logic
  - Changes to the upstream report-facts.json structure
  - Changes to the survey_data schema

When a test fails after a legitimate change, update the expected value here
AND note the change in scripts/gen-survey-json.py docstring.
"""

import json
from collections import Counter
from pathlib import Path

import pytest

